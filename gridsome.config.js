const path = require('path')

function addStyleResource (rule) {
  rule.use('style-resource')
    .loader('style-resources-loader')
    .options({
      patterns: [
        path.resolve(__dirname, './src/style/_globals.scss'),
        path.resolve(__dirname, './src/style/_variables.scss'),
      ],
    })
}

